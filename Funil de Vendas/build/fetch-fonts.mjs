// Downloads self-hosted woff2 for Archivo (variable wght+wdth), Inter, IBM Plex Mono
// from the Google Fonts CSS2 API (all three are SIL OFL — free to self-host/redistribute),
// then writes client_assets/fonts/fonts.css with localized url() references.
import { mkdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const FONTS_DIR = join(HERE, "..", "client_assets", "fonts");
mkdirSync(FONTS_DIR, { recursive: true });

// Modern Chrome UA so the API returns woff2 (not ttf).
const UA =
  "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/124.0.0.0 Safari/537.36";

const SOURCES = [
  // Archivo: variable weight AND width axis -> @font-face gets font-weight + font-stretch ranges.
  { fam: "Archivo", url: "https://fonts.googleapis.com/css2?family=Archivo:wght@100..900&display=swap",
    urlWdth: "https://fonts.googleapis.com/css2?family=Archivo:wdth,wght@62..125,100..900&display=swap" },
  { fam: "Inter", url: "https://fonts.googleapis.com/css2?family=Inter:wght@400..800&display=swap" },
  { fam: "IBM Plex Mono", url: "https://fonts.googleapis.com/css2?family=IBM+Plex+Mono:wght@400;500;600&display=swap" },
];

async function getCss(url) {
  const r = await fetch(url, { headers: { "User-Agent": UA } });
  if (!r.ok) throw new Error(`CSS ${url} -> ${r.status}`);
  return r.text();
}

let combined = "/* Self-hosted fonts (SIL OFL): Archivo, Inter, IBM Plex Mono. Generated by fetch-fonts.mjs */\n";
let counter = 0;
const seen = new Map();

async function processCss(css, famSlug) {
  // Split into @font-face blocks, localize each url().
  const blocks = css.split("@font-face").slice(1);
  let out = "";
  for (const b of blocks) {
    const block = "@font-face" + b.slice(0, b.indexOf("}") + 1);
    const urlMatch = block.match(/url\((https:\/\/[^)]+\.woff2)\)/);
    if (!urlMatch) continue;
    const woffUrl = urlMatch[1];
    let local = seen.get(woffUrl);
    if (!local) {
      const rangeMatch = block.match(/unicode-range:\s*([^;]+);/);
      const tag = rangeMatch && /U\+0000/.test(rangeMatch[1]) ? "latin" :
                  rangeMatch && /U\+0100/.test(rangeMatch[1]) ? "latinext" : `r${counter}`;
      local = `${famSlug}-${tag}-${counter++}.woff2`;
      const rr = await fetch(woffUrl, { headers: { "User-Agent": UA } });
      if (!rr.ok) throw new Error(`woff2 ${woffUrl} -> ${rr.status}`);
      const buf = Buffer.from(await rr.arrayBuffer());
      writeFileSync(join(FONTS_DIR, local), buf);
      seen.set(woffUrl, local);
      console.log(`  saved ${local} (${(buf.length / 1024).toFixed(1)} KB)`);
    }
    out += block.replace(woffUrl, `./${local}`) + "\n";
  }
  return out;
}

for (const s of SOURCES) {
  console.log(`Fetching ${s.fam} ...`);
  const slug = s.fam.toLowerCase().replace(/\s+/g, "");
  if (s.urlWdth) {
    try {
      const cssW = await getCss(s.urlWdth);
      combined += await processCss(cssW, slug);
      console.log(`  ${s.fam}: width+weight variable OK`);
      continue;
    } catch (e) {
      console.log(`  ${s.fam}: width axis failed (${e.message}); falling back to weight-only`);
    }
  }
  const css = await getCss(s.url);
  combined += await processCss(css, slug);
}

writeFileSync(join(FONTS_DIR, "fonts.css"), combined);
console.log(`\nWrote ${join(FONTS_DIR, "fonts.css")}`);
console.log("DONE");
